  SignUpState();
}

class InitialSignUpState extends SignUpState {
  InitialSignUpState();
}

class LoadingSignUpState extends SignUpState {
  LoadingSignUpState();
}

class SuccessSignUpState extends SignUpState {
  SuccessSignUpState();
}

class FailedSignUpState extends SignUpState {
  FailedSignUpState();
}
